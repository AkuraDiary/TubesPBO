    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        Di.approvePembelianPresenter.updateStatusPembelian(
                cbActionPembelian.getSelectedItem().toString()
        );

        if(Di.approvePembelianPresenter.statusUpdatePembelian == -2){
            showMessageDialog(null, "Pilih data pembelian terlebih dahulu");
            return;
        }
        if(Di.approvePembelianPresenter.statusUpdatePembelian == -1){
            showMessageDialog(null, "Gagal update data pembelian");
            return;
        }
        showMessageDialog(null, "Berhasil update data pembelian");
        populateTablePembelian();
        resetField();
    }//GEN-LAST:event_bSimpanActionPerformed


